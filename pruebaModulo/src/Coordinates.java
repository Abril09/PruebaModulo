
public class Coordinates {
    private  int file;
    private int column;

    public Coordinates(int file, int column) {
        this.file = file;
        this.column = column;
    }

    public int getFile() {
        return file;
    }

    public void setFile(int file) {
        this.file = file;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }

    
}